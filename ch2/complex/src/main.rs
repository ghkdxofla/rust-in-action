use num::complex::Complex;

fn main() {
    let a = Complex { re: 2.1, im: -1.2 }; // 생성자 대신 리터럴 형태로 사용
    let b = Complex::new(11.1, 22.2);
    let result = a + b;

    println!("{} + {}i", result.re, result.im);
}

